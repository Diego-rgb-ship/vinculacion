  <div class="modal video-modal fade" id="politicasgeneralidades" tabindex="-1" role="dialog" aria-labelledby="politicasgeneralidades">
    <div class="modal-dialog" role="document">
      <div class="modal-content">
        <div class="modal-header">
          POLÍTICAS DE OPERACIÓN
          <button type="button" class="close" data-dismiss="modal" aria-label="Close"><span aria-hidden="true">&times;</span></button>            
        </div>
          <div class="modal-body">
            <p>
              Los estudiantes pueden prestar su servicio social una vez aprobado el 70% de los créditos de los planes de estudio.
          </p>
          <p>
            La duración del servicio social no podrá ser menor de 480 y un máximo de 500 horas (Sistema de Asignación y Transferencia de Créditos Académicos).
          </p>
          <p> 
            La duración del Servicio Social es no menor de 6 meses ni mayor de 2 años.
          </p>
          <p>
            La evaluación  será  realizada  por  el  responsable  del  programa  de Servicio Social que asesoró y supervisó las actividades de la dependencia 
            que reciba los beneficios de prestadores de servicio. En la evaluación se considera el nivel de desempeño y competencias alcanzadas de sentido de responsabilidad y de servicio.
          </p>
          <p>
            Los prestadores de servicio que no acumulen en un solo organismo el total de horas requeridas para el Servicio Social, considerando las características propias del programa, o por razones no atribuibles al prestador del servicio (programas inferiores a 480 horas, cancelación de programas, cierre de la entidad, entre otros), el prestador del Servicio Social podrá continuar en otro programa.
          </p>
          <p>
            Los prestadores que trabajen en dependencias federales, estatales y municipales y opten por acreditar el Servicio Social, deberán cumplir con los siguientes requisitos:
          </p>
          <p>
            a) Presentar documentación que lo acredite como trabajador con una antigüedad mínima de 6 meses.
          </p>
          <p>
            b) Presentar documentos   que   justifiquen   que   las   labores realizadas en el organismo y área de adscripción, corresponden y cumplen con el objetivo que se pretende alcanzar al prestar el Servicio Social.
          </p>
          <p>
            c) Respetar y cumplir las obligaciones establecidas del prestador del Servicio Social.
          </p>
          <p>
            d) El informe final  deberá  corresponder  a  las  actividades realizadas a partir de la solicitud de prestación del Servicio Social y autorización respectiva.
          </p>
          <p>
            Para la  realización  del  Servicio  Social  en  un  ámbito  de  influencia diferente al del Instituto Tecnológico, los prestadores deberán cubrir  los siguientes requisitos:
          </p>
          <p>
            a) Elaborar solicitud de autorización para realizar el Servicio Social en otras instituciones pertenecientes al Espacio Común de la Educación Superior Tecnológica, Instituto Politécnico Nacional o Universidades Estatales.
          </p>
          <p>
            b) Entregar a la dependencia seleccionada, oficio de autorización expedido por el Instituto Tecnológico de procedencia, indicándole que no hay inconveniente en que se le coordine su actividad de Servicio Social.
          </p>
          <p>
            c) Verificar cuando concluya su programa de Servicio Social, que la institución seleccionada envíe el formato de evaluación y la carta de terminación al Instituto Tecnológico de procedencia
          </p>
          <p>
             Los programas  de  Servicio  Social  propios  del  Instituto  Tecnológico relacionados con actividades culturales, deportivas y cívicas, solamente se autorizan con carácter de promotores y/o instructores, entre otros y cuando sea en beneficio a la sociedad.
          </p>

