          </p>   
          <p>  
            Entrega al Departamento de Vinculación el formato de Evaluación de Servicio Social en un plazo no mayor de 20 días hábiles, después de haber concluido el mismo, de lo contrario es susceptible de no acreditarlo.
          </p>
